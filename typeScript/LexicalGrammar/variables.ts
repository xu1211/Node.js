
/**
声明变量的类型(值为 undefined):
  var [变量名] : [类型];

声明变量的类型及初始值:
  var [变量名] : [类型] = 值;

声明任意类型(值为 undefined):
  var [变量名];

声明任意类型及初始值:
  var [变量名] = 值;
 */


var uname1: string;
uname1 = "1"
console.log(typeof uname1)

var uname2: string = "Runoob";
console.log(typeof uname2)

var uname3;
console.log(typeof uname3)

var uname4 = "Runoob";
console.log(typeof uname4)

